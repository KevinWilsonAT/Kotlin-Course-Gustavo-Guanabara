fun main(args: Array<String>) {

    val n1:Int = 4
    val n2:Int = 5

    println("Is $n1 greater than $n2? {n1 > n2}")
    println("Is $n1 lesser than $n2? ${n1 < n2}")
    println("Is $n1 greater or equals to $n2? ${n1 >= n2}")
    println("Is $n1 lesser or equals to $n2? ${n1 <= n2}")
    println("Is $n1 equals to $n2? ${n1 == n2}")
    println("Is $n1 different than $n2? ${n1 != n2}")
}