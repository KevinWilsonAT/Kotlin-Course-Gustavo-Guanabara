fun main(args: Array<String>) {

    val n1:Int = 5
    val n2:Int = 2

    println("$n1 + $n2 = ${n1 + n2}") // println("$n1 + $n2 = ${n1.plus(n2)}")
    println("$n1 - $n2 = ${n1 - n2}") // println("$n1 - $n2 = ${n1.minus(n2)}")
    println("$n1 * $n2 = ${n1 * n2}") // println("$n1 * $n2 = ${n1.times(n2)}")
    println("$n1 / $n2 = ${n1 / n2}") // println("$n1 / $n2 = ${n1.div(n2)}")
    println("$n1 % $n2 = ${n1 % n2}") // println("$n1 % $n2 = ${n1.mod(n2)} or ${n1.rem(n2}")
}