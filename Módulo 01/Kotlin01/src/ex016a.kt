fun main(args: Array<String>) {

    var tv:Int = 4
    println("You had $tv Televisions at home")
    tv += 3
    println("Now, you have $tv Televisions at home\n")

    var busTicket:Float = 8.65f
    println("The cost of the bus ticket was $${busTicket}")
    busTicket += 0.25f
    println("Now, the bus ticket cost is $${busTicket}")
}